function dateDiff($start, $end, $format = 1){
	$result = "";
	$datetime1 = date_create(date("Y-m-d", mktime(0,0,0,date('m',$start),1,date('Y',$start))));
	$datetime2 = date_create(date('Y-m-d', mktime(0,0,0,date('m',$end)+1,1,date('Y',$end))));
	$ddiff = date_diff($datetime1, $datetime2);
	$year = (int)$ddiff->format("%Y");
	$month = (int)$ddiff->format("%M");
	switch($year) {
		case 0: $year = ""; break;
		case 1:
		case 21:
		case 31: $year .= " год"; break;
		case 2:
		case 3:
		case 4:
		case 22:
		case 23:
		case 24:
		case 32:
		case 33:
		case 34: $year .= " года"; break;
		default: $year.=" лет"; break;
	}
	switch($month) {
		case 0: $month = ""; break;
		case 1: $month = "1 месяц"; break;
		case 2:
		case 3:
		case 4: $month .= " месяца"; break;
		default: $month.=" месяцев"; break;
	}
	$result = $format==1?$year:$year." ".$month;
	return trim($result);
}
